public enum ActivityLevel : int
{
    Sedentary = 0,          // Little to no exercise.
    LightlyActive = 1,      // Exercise 1-3 time/week
    ModeratelyActive = 2,   // Daily exercise or intense exercise 3-4 times/week 
    VeryActive = 3,         // Intense exercise 6-7 times/week
    SuperActive = 4         // Very intense exercise daily, or physical job
}
